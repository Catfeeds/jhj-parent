package com.jhj.vo.user.chart;

import java.util.List;

public class Series {
	
	private String name;
	
	private String type;
	
	private List  data;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public List getData() {
		return data;
	}

	public void setData(List data) {
		this.data = data;
	}

	

}
